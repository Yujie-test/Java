package chapter19.array.homework;

/*
    所有武器的父类
 */

public class Weapon {
    String name;
    int attack;
    int move;

    public int getAttack() {
        return attack;
    }

    public void setAttack(int attack) {
        this.attack = attack;
    }

    public int getMove() {
        return move;
    }

    public void setMove(int move) {
        this.move = move;
    }
}
